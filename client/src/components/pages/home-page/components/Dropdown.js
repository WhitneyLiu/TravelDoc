import { Menu, Transition } from "@headlessui/react";
import { Fragment, useEffect, useRef, useState } from "react";
import { ChevronDownIcon } from "@heroicons/react/20/solid";
import HelloSign from "hellosign-embedded";

const client = new HelloSign();

const openSignatureRequest = (signUrl, clientId) => {
  client.open(signUrl, {
    clientId: clientId,
    skipDomainVerification: true,
  });
};

export default function Example({
  pdf,
  pdfList,
  setIframeSrc1,
  setIframeSrc2,
}) {
  const [signUrl, setSignUrl] = useState(null);

  useEffect(() => {
    // Fetch the sign_url from your server when the component mounts
    fetch(
      "https://frwfi2fosa.execute-api.us-east-1.amazonaws.com/dev/dropbox-sign"
    )
      .then((data) => setSignUrl(data.signUrl))
      .catch((err) => console.error("Failed to fetch sign_url:", err));
  }, []);

  function handleView({ pdf, pdfList, setIframeSrc1, setIframeSrc2 }) {
    if (!pdf || !pdf.url) {
      console.error("Invalid PDF or URL");
      return;
    }

    // Set the URL for the left iframe to the selected PDF
    setIframeSrc1(pdf.url);

    // Extract the base name of the contract
    const urlParts = pdf.url.split("/");
    const fileName = urlParts[urlParts.length - 1];
    const baseName = fileName.split("-")[0];

    // Find the corresponding 'Explanation' PDF
    const explanationPdf = pdfList.find(
      (item) => item && item.url && item.url.includes(`${baseName}Explanation`)
    );

    if (explanationPdf && explanationPdf.url) {
      console.log("Setting explanation PDF:", explanationPdf.url);
      setIframeSrc2(explanationPdf.url);
    } else {
      console.warn("Explanation PDF not found");
      setIframeSrc2("");
    }
  }

  return (
    <div className="">
      <Menu as="div" className="relative inline-block text-left">
        <div>
          <Menu.Button className="inline-flex w-full justify-center rounded-md bg-black bg-opacity-40 px-4 py-2 text-sm font-medium text-white hover:bg-opacity-30 focus:outline-none focus-visible:ring-2 focus-visible:ring-white focus-visible:ring-opacity-75">
            Options
            <ChevronDownIcon
              className="ml-2 -mr-1 h-5 w-5 text-violet-200 hover:text-violet-100"
              aria-hidden="true"
            />
          </Menu.Button>
        </div>
        <Transition
          as={Fragment}
          enter="transition ease-out duration-100"
          enterFrom="transform opacity-0 scale-95"
          enterTo="transform opacity-100 scale-100"
          leave="transition ease-in duration-75"
          leaveFrom="transform opacity-100 scale-100"
          leaveTo="transform opacity-0 scale-95"
        >
          <Menu.Items className="absolute right-0 mt-2 w-56 origin-top-right divide-y divide-gray-100 rounded-md bg-white shadow-lg ring-1 ring-black ring-opacity-5 focus:outline-none">
            <div className="px-1 py-1 ">
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? "bg-violet-500 text-white" : "text-gray-900"
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                    onClick={() => {
                      if (signUrl) {
                        openSignatureRequest(
                          signUrl,
                          "04024c77ff759c89d0ac84882057ab29"
                        );
                      } else {
                        console.warn("Sign URL not available");
                      }
                    }}
                  >
                    {active ? (
                      <EditActiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    ) : (
                      <EditInactiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    )}
                    Sign
                  </button>
                )}
              </Menu.Item>
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? "bg-violet-500 text-white" : "text-gray-900"
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                    onClick={() =>
                      handleView({ pdf, pdfList, setIframeSrc1, setIframeSrc2 })
                    }
                  >
                    {active ? (
                      <MoveActiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    ) : (
                      <MoveInactiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    )}
                    View
                  </button>
                )}
              </Menu.Item>
            </div>
          </Menu.Items>
        </Transition>
      </Menu>
    </div>
  );
}

